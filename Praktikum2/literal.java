// File: Literal.java 
public class literal {
    public static void main(String[] args) {
        // Literal bilangan
        System.out.println(17); // Decimal
        System.out.println(017); // Octal
        System.out.println(0x17); // Hexadecimal

        // Literal float
        System.out.println(0.1); // Float literal
        System.out.println(-2677777); // Valid float number

        // Literal double
        System.out.println(2e45); // Double literal
        System.out.println(2E45);
        System.out.println(2E+45);
        System.out.println(2E-45);
        System.out.println(2e4F); // Float notation

        // Literal karakter dan string
        System.out.println('A'); // Karakter tunggal
        System.out.println("Hello World!"); // String
    }
}
